# 1) 산술 연산자 - 1
print (1 + 1)
print (3 - 2)
print (5 * 2)
print (6 / 3)   # 정수 '2'가 아닌 실수 '2.0'으로 표현
print("============================================")
# 2) 산술 연산자 - 2
print (2 ** 3)  # 2의 3제곱 => 2*2*2 = 8
print (10 % 3)  # '10 / 3'의 나머지값 => 10 / 3 = 3 --- "1"
print (10 // 3) # '10 / 3'의 몫값 => 10 / 3 = "3" --- 1
print("============================================")
# 3) 비교 연산자 - 1
print(10 > 3)
print(4 >= 7)
print(10 < 3)
print(5 <= 5)
print("============================================")
# 4) 비교 연산자 - 2
print(3 == 3)
print(4 == 7)
print(3 + 4 == 7)
print(1 != 5)
print("============================================")
# 5) 논리 연산자 
print((3 > 0) and (3 > 5))
print((3 > 0) or (3 > 5))
print(not(1 != 3))
print("============================================")