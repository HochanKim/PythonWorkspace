import math # math 모듈의 기능을 가져다 쓰겠다는 의미

# 'math.'와 같이 작성
result = math.floor(4.99)
print(result)   # 4.99의 내림

result = math.ceil(3.14)
print(result)   # 3.14의 올림

result = math.sqrt(16)
print(result)   # 16의 제곱근

